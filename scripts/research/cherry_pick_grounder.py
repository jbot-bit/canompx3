"""Cherry-pick grounder -- attach a literature-grounded theory_citation to a
bridge-generated heavyweight Chordia DRAFT prereg.

See ``docs/specs/fast_lane_state_graph.md`` for the canonical fast-lane chain definition.

Closes the gap between fast_lane_to_heavyweight_bridge.py (which deliberately
omits theory_citation per the field-presence trap, memory/feedback_chordia_
theory_citation_field_presence_trap.md) and an active heavyweight prereg in
docs/audit/hypotheses/. Without grounding, every bridge draft sits at the
strict no-theory hurdle (t >= 3.79); successful grounding earns t >= 3.00 per
Criterion 4 (with-theory tier, Chordia 2018).

Workflow
--------
1. Read a bridge draft (docs/audit/hypotheses/drafts/*-chordia-heavyweight-v1.draft.yaml)
2. Load the local lit corpus (docs/institutional/literature/*.md) via the
   canonical lhp.literature_index.load_corpus.
3. Run search_corpus against the strategy mechanism (built from the draft's
   scope fields). If zero results: emit GROUNDED_VERDICT=NO_LOCAL_LIT and
   refuse (web fallback is operator-driven, not automated -- v1 ships without
   live web calls; trigger flag reserved for v2).
4. If results: call propose_with_mock_support to pick a citation + write a
   1-2 sentence economic_basis grounded in the top extract.
5. verify_citation_content gates the LLM output. Pass -> write <slug>.grounded.yaml
   with theory_citation + theory_grant=true. Fail -> write
   <slug>.grounded.rejected.txt and exit non-zero.

Cherry-picked (reused, not rewritten)
-------------------------------------
- scripts/research/lhp/literature_index.load_corpus, search_corpus,
  verify_citation_content, format_search_results_for_llm
- scripts/research/lhp/llm_client.propose_with_mock_support,
  CostCeilingExceeded, LLMRefusalToGround, LLMRequestError
- scripts/research/lhp/yaml_emitter.write_rejected (refusal sidecar pattern)

Doctrine grounding
------------------
- Field-presence trap defense: feedback_chordia_theory_citation_field_presence_trap.md
- LHP-validator quarantine pattern: feedback_lhp_validator_vs_field_presence_trap_n1.md
- Local-first lit grounding: institutional-rigor.md section 7 + Local Academic Grounding Rule
- Criterion 4 with-theory tier: docs/institutional/pre_registered_criteria.md
- Stage file: docs/runtime/stages/2026-05-19-cherry-pick-grounder.md

This script does NOT write to chordia_audit_log.yaml, validated_setups,
lane_allocation.json, or any file under trading_app/live/. It only writes to
docs/audit/hypotheses/drafts/<slug>.grounded.yaml (PASS) or
<slug>.grounded.rejected.txt (REFUSED). The original draft is never mutated.
"""

